sale_price = 29.99

# Call a lambda function adding 20% to sale_price
print((lambda x: x * 1.2)(sale_price))

'''
O código acima demonstra como usar uma função lambda em uma linha só em Python. A função lambda é definida para calcular o preço com imposto, multiplicando o preço de venda por 1.2. A função é chamada diretamente com o preço de venda, e o resultado é impresso. Funções lambda são úteis para criar funções pequenas e anônimas de forma concisa.
'''