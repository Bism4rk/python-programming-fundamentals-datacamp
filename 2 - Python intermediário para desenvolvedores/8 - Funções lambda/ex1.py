sale_price = 29.99

# Define a lambda function called add_tax
add_tax = lambda x: x * 1.2

# Call the lambda function
print(add_tax(sale_price))

'''
O código acima demonstra como usar uma função lambda em Python. A função `add_tax` é definida para calcular o preço com imposto, multiplicando o preço de venda por 1.2. A função é chamada com o preço de venda, e o resultado é impresso. Funções lambda são úteis para criar funções pequenas e anônimas de forma concisa.
'''