# Print the result of 2/1 and 1/2
print(2/1)
print(1/2)

# Print the floored division result of 2//1 and 1//2
print(2//1)
print(1//2)

# Print the type of 2/1 and 2//1
print(type(2/1))
print(type(2//1))

'''
O código acima demonstra a diferença entre a divisão normal e a divisão inteira em Python. A divisão normal (/) retorna um número de ponto flutuante, enquanto a divisão inteira (//) retorna o maior inteiro menor ou igual ao resultado da divisão. O tipo de dado resultante também é exibido, mostrando que a divisão normal resulta em um float e a divisão inteira em um int.
'''